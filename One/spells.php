<br/>Level:	Brd 2, Clr 2, Sor/Wiz 2
<br/>Components:	V, M/DF
<br/>Casting Time:	1 standard action
<br/>Range:	Touch
<br/>Target:	Object touched
<br/>Duration:	10 min./level (D)
<br/>Saving Throw:	None
<br/>Spell Resistance:	No
<br/>This spell causes an object to radiate shadowy illumination out to a 20-foot radius. All creatures in the area gain concealment (20% miss chance). Even creatures that can normally see in such conditions (such as with darkvision or low-light vision) have the miss chance in an area shrouded in magical darkness.
<br/><br/>
Normal lights (torches, candles, lanterns, and so forth) are incapable of brightening the area, as are light spells of lower level. Higher level light spells are not affected by darkness.
<br/><br/>
If darkness is cast on a small object that is then placed inside or under a lightproof covering, the spell’s effect is blocked until the covering is removed.
<br/><br/>
Darkness counters or dispels any light spell of equal or lower spell level.
<br/><br/>
