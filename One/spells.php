    <td class="q">Gaseous Form</td>
    <td class="a">Transmutation
<br/>Level:	Air 3, Brd 3, Sor/Wiz 3
<br/>Components:	S, M/DF
<br/>Casting Time:	1 standard action
<br/>Range:	Touch
<br/>Target:	Willing corporeal creature touched
<br/>Duration:	2 min./level (D)
<br/>Saving Throw:	None
<br/>Spell Resistance:	No
<br/>The subject and all its gear become insubstantial, misty, and translucent. Its material armor (including natural armor) becomes worthless, though its size, Dexterity, deflection bonuses, and armor bonuses from force effects still apply. The subject gains damage reduction 10/magic and becomes immune to poison and critical hits. It can’t attack or cast spells with verbal, somatic, material, or focus components while in gaseous form. (This does not rule out the use of certain spells that the subject may have prepared using the feats Silent Spell, Still Spell, and Eschew Materials.) The subject also loses supernatural abilities while in gaseous form. If it has a touch spell ready to use, that spell is discharged harmlessly when the gaseous form spell takes effect.
<br/><br/>
A gaseous creature can’t run, but it can fly at a speed of 10 feet (maneuverability perfect). It can pass through small holes or narrow openings, even mere cracks, with all it was wearing or holding in its hands, as long as the spell persists. The creature is subject to the effects of wind, and it can’t enter water or other liquid. It also can’t manipulate objects or activate items, even those carried along with its gaseous form. Continuously active items remain active, though in some cases their effects may be moot.
<br/><br/>
Arcane Material Component
<br/>A bit of gauze and a wisp of smoke.
