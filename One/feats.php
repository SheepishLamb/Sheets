    <td class="a2 orange">7</td>
    <td class="q">Weapon Focus - Greatsword</td>
    <td class="a"><b>Prerequisites</b>
<br/>Proficiency with selected weapon, base attack bonus +1.
<br/><br/>
<b>Benefit</b>
<br/>You gain a +1 bonus on all attack rolls you make using the selected weapon.
<br/><br/>
<b>Special</b>
<br/>You can gain this feat multiple times. Its effects do not stack. Each time you take the feat, it applies to a new type of weapon.
<br/><br/>
A fighter may select Weapon Focus as one of his fighter bonus feats. He must have Weapon Focus with a weapon to gain the Weapon Specialization feat for that weapon.
</td>
    <td class="a1">Player's Handbook, pg. 99</td>
