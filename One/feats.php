You also gain a +4 bonus on any attack roll made to attack an object held or carried by another character.
<br/><br/>
<b>Normal</b>
<br/>Without this feat, you provoke an attack of opportunity when you strike at an object held or carried by another character.
<br/><br/>
<b>Special</b>
<br/>A fighter may select Improved Sunder as one of his fighter bonus feats.
</td>
