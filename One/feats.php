    <td class="a2 orange">3</td>
    <td class="q">Cleave</td>
    <td class="a"><b>Prerequisite</b>
<br/>Str 13, Power Attack
<br/><br/>
<b>Benefit</b>
<br/>If you deal a creature enough damage to make it drop (typically by dropping it to below 0 hit points or killing it), you get an immediate, extra melee attack against another creature within reach. You cannot take a 5-foot step before making this extra attack. The extra attack is with the same weapon and at the same bonus as the attack that dropped the previous creature. You can use this ability once per round.
<br/><br/>
<b>Special</b>
<br/>A fighter may select Cleave as one of his fighter bonus feats.</td>
    <td class="a1">Player's Handbook, pg. 98</td>
