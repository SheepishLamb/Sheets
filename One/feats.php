    <td class="q">Quick Draw</td>
    <td class="a"><b>Prerequisite</b>
<br/>Base attack bonus +1.
<br/><br/>
<b>Benefit</b>
<br/>You can draw a weapon as a free action instead of as a move action. You can draw a hidden weapon (see the Sleight of Hand skill) as a move action.
<br/><br/>
A character who has selected this feat may throw weapons at his full normal rate of attacks (much like a character with a bow).
<br/><br/>
<b>Normal</b>
<br/>Without this feat, you may draw a weapon as a move action, or (if your base attack bonus is +1 or higher) as a free action as part of movement. Without this feat, you can draw a hidden weapon as a standard action.
<br/><br/>
<b>Special</b>
<br/>A fighter may select Quick Draw as one of his fighter bonus feats.
