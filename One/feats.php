    <td class="q">Improved Critical - Greatsword</td>
    <td class="a"><b>Prerequisites</b>
<br/>Proficient with weapon, base attack bonus +8.
<br/><br/>
<b>Benefit</b>
<br/>When using the weapon you selected, your threat range is doubled.
<br/><br/>
<b>Special</b>
<br/>You can gain Improved Critical multiple times. The effects do not stack. Each time you take the feat, it applies to a new type of weapon.
<br/><br/>
This effect doesn’t stack with any other effect that expands the threat range of a weapon.
<br/><br/>
A fighter may select Improved Critical as one of his fighter bonus feats.
