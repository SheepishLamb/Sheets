    <td class="a2 blue">2</td>
    <td class="q">Power Attack</td>
    <td class="a"><b>Prerequisite</b>
<br/>Str 13
<br/><br/>
<b>Benefit</b>
<br/>On your action, before making attack rolls for a round, you may choose to subtract a number from all melee attack rolls and add the same number to all melee damage rolls. This number may not exceed your base attack bonus. The penalty on attacks and bonus on damage apply until your next turn.
<br/><br/>
<b>Special</b>
<br/>If you attack with a two-handed weapon, or with a one-handed weapon wielded in two hands, instead add twice the number subtracted from your attack rolls. You can’t add the bonus from Power Attack to the damage dealt with a light weapon (except with unarmed strikes or natural weapon attacks), even though the penalty on attack rolls still applies. (Normally, you treat a double weapon as a one-handed weapon and a light weapon. If you choose to use a double weapon like a two-handed weapon, attacking with only one end of it in a round, you treat it as a two-handed weapon.)
<br/><br/>A fighter may select Power Attack as one of his fighter bonus feats.</td>
    <td class="a1">Player's Handbook, pg. 98</td>
